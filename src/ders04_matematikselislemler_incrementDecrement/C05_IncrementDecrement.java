package ders04_matematikselislemler_incrementDecrement;

public class C05_IncrementDecrement {

    public static void main(String[] args) {

        int sayi1=10;
        sayi1= sayi1+3;
        sayi1 +=3;
        System.out.println(sayi1);


        int sayi2=10;
        sayi2= sayi2+1;
        sayi2 +=2;
        sayi2++;
        System.out.println(sayi2);

        int sayi3=10;
        sayi3=sayi3-1;
        sayi3--;
        --sayi3;
        System.out.println(sayi3);

    }

}
