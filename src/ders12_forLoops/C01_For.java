package ders12_forLoops;

public class C01_For {
    public static void main(String[] args) {
        String str="Java\n";
        System.out.println(str.repeat(10));

        // 1 den yüze kadar yazdırın
        // 3 basamaklı 23 ile bölünebilen sayıları yazdırın

        for (int i = 1; i <101 ; i++) {
            System.out.print(i+" ");

        }

    }
}
