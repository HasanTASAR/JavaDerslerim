package ders09_StringManipulations;

public class C04_charAt {
    public static void main(String[] args) {
        String str = "Java Candir";
        System.out.println(str.charAt(0));//ilk karakteri verir. J
        System.out.println(str.charAt(5));//6. karakteri verir.  C
        System.out.println(str.charAt(10));//11 karakteri verir. r
        /*
        karakter say覺s覺 ile indeks farkl覺d覺r. indeks+1= karakter
         */
    }
}
